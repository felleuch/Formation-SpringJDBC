package com.wha.spring.jdbc.model;

public class Employee {
	
	
	public int id;
	
	public String name;
	
	public String role;
	
	// getter setters
	

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}
	
	
	
	

}
